#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	x;
	size_t	y;

	y = 0;
	x = ft_strlen(s1);
	while (y < n)
		s1[x++] = s2[y++];
	s1[x] = '\0';
	return (s1);
}
