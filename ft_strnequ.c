#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t x;

	x = 0;
	while (s1[x] || (s2[x] && x < n))
	{
		if (s1[x] != s2[x])
			return (0);
		x++;
	}
	return (1);
}
