#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int	x;

	x = 0;
	while (s1[x] || s2[x])
	{
		if (s1[x] != s2[x])
			return (0);
		x++;
	}
	return (1);
}
