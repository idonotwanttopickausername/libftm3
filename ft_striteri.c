#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int x;

	if (s && f)
	{
		x = 0;
		while (s[x])
		{
			f(x, (s + x));
			x++;
		}
	}
}
